// SHA-256 / HMAC-SHA256 / HKDF / getrandom tests

use viv::core::crypto::sha256::{Sha256, hkdf_expand, hkdf_extract, hmac_sha256};

// ── getrandom ────────────────────────────────────────────────────────

#[test]
fn getrandom_fills_buffer() {
    let mut buf = [0u8; 32];
    viv::core::crypto::getrandom(&mut buf).unwrap();
    assert_ne!(buf, [0u8; 32]);
}

