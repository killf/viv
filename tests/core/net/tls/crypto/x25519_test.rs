// ── Base point scalar mult (identity checks) ────────────────────────

#[test]
fn scalarmult_base_point_known() {
    // scalarmult(1, 9) should give 9 back (identity scalar after clamping won't be 1)
    // Instead test: scalarmult with a known scalar against base point
    // We already test this via DH vectors, but let's also test iterated
    let base_point = {
        let mut bp = [0u8; 32];
        bp[0] = 9;
        bp
    };

    // RFC 7748 Section 5.2: iterated test
    // After 1 iteration: scalarmult(k=9, u=9)
    let mut k = base_point;
    let mut u = base_point;
    for _ in 0..1 {
        let new_k = scalarmult(&k, &u);
        u = k;
        k = new_k;
    }
    let expected_after_1: [u8; 32] = [
        0x42, 0x2c, 0x8e, 0x7a, 0x62, 0x27, 0xd7, 0xbc,
        0xa1, 0x35, 0x0b, 0x3e, 0x2b, 0xb7, 0x27, 0x9f,
        0x78, 0x97, 0xb8, 0x7b, 0xb6, 0x85, 0x4b, 0x78,
        0x3c, 0x60, 0xe8, 0x03, 0x11, 0xae, 0x30, 0x79,
    ];
    assert_eq!(k, expected_after_1);
}

// ── RFC 7748 Section 5.2: iterated 1000 times ──────────────────────

#[test]
