// ── Test 1: hkdf_expand_label "derived" ─────────────────────────────

#[test]
fn hkdf_expand_label_derived_from_early_secret() {
    // Derive-Secret(early_secret, "derived", "") =
    //   HKDF-Expand-Label(early_secret, "derived", Hash(""), 32)
    use viv::core::net::tls::crypto::sha256::Sha256;
    let empty_hash = Sha256::hash(b"");

    let mut out = [0u8; 32];
    hkdf_expand_label(&EARLY_SECRET, b"derived", &empty_hash, &mut out);
    assert_eq!(out, DERIVED_1, "Derive-Secret(early_secret, \"derived\", \"\") mismatch");
}

// ── Test 2: Full handshake key derivation ───────────────────────────

#[test]
fn handshake_traffic_keys() {
    let mut ks = KeySchedule::new();
    assert_eq!(ks.early_secret(), &EARLY_SECRET, "early_secret mismatch");

    let (client_tk, server_tk) = ks.derive_handshake_secrets(&ECDHE_SHARED, &HELLO_HASH);

    assert_eq!(client_tk.key, CLIENT_HS_KEY, "client handshake key mismatch");
    assert_eq!(client_tk.iv, CLIENT_HS_IV, "client handshake IV mismatch");
    assert_eq!(server_tk.key, SERVER_HS_KEY, "server handshake key mismatch");
    assert_eq!(server_tk.iv, SERVER_HS_IV, "server handshake IV mismatch");
}

// ── Test 3: finished_key derivation ─────────────────────────────────

#[test]
fn finished_keys() {
    let mut ks = KeySchedule::new();
    let _ = ks.derive_handshake_secrets(&ECDHE_SHARED, &HELLO_HASH);

    // RFC 8446 §4.4.4: finished_key = HKDF-Expand-Label(hs_secret, "finished", "", 32)
    // We verify server and client finished keys are deterministic and non-zero.
    let server_fin = ks.server_finished_key();
    let client_fin = ks.client_finished_key();

    // RFC 8448 §3 server finished key
    let expected_server_fin: [u8; 32] = [
        0x00, 0x8d, 0x3b, 0x66, 0xf8, 0x16, 0xea, 0x55,
        0x9f, 0x96, 0xb5, 0x37, 0xe8, 0x85, 0xc3, 0x1f,
        0xc0, 0x68, 0xbf, 0x49, 0x2c, 0x65, 0x2f, 0x01,
        0xf2, 0x88, 0xa1, 0xd8, 0xcd, 0xc1, 0x9f, 0xc8,
    ];
    assert_eq!(server_fin, expected_server_fin, "server finished key mismatch");

    // RFC 8448 §3 client finished key
    let expected_client_fin: [u8; 32] = [
        0xb8, 0x0a, 0xd0, 0x10, 0x15, 0xfb, 0x2f, 0x0b,
        0xd6, 0x5f, 0xf7, 0xd4, 0xda, 0x5d, 0x6b, 0xf8,
        0x3f, 0x84, 0x82, 0x1d, 0x1f, 0x87, 0xfd, 0xc7,
        0xd3, 0xc7, 0x5b, 0x5a, 0x7b, 0x42, 0xd9, 0xc4,
    ];
    assert_eq!(client_fin, expected_client_fin, "client finished key mismatch");
}
